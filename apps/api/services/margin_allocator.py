"""Динамическое распределение маржи между готовыми кандидатами цикла.

Идея (по ТЗ Капитана): когда сетап прошёл ВСЕ гейты и готов к публикации,
система смотрит, сколько ещё кандидатов прошло гейты В ЭТОМ ЖЕ цикле, и делит
СВОБОДНУЮ маржу (потолок − уже открытые) поровну между ними. Если кандидат один —
он занимает всю свободную маржу (максимальный размер на готовую идею).

Чистая функция над числами (free_margin, num_ready) — без settings/ccxt/БД,
тестируется изолированно. Возвращает МАРЖУ на сделку (USDT); нотионал = маржа ×
плечо считает trade_plan (плечо — отдельная регулируемая ручка DYNAMIC_MARGIN_LEVERAGE).
"""
from __future__ import annotations


def per_trade_margin(free_margin: float, num_ready: int,
                     *, cap_pct_of_free: float = 1.0) -> float:
    """Маржа (USDT) на одну готовую сделку этого цикла.

    free_margin   — свободная маржа сейчас: потолок(70% экв) − занятая открытыми.
    num_ready     — сколько кандидатов прошло ВСЕ гейты в этом цикле (>=1).
    cap_pct_of_free — верхний предохранитель на долю free на одну сделку
                      (1.0 = одинокий кандидат берёт всё, как просил Капитан).

    Правила:
      num_ready <= 1 → вся свободная маржа (× cap_pct_of_free).
      num_ready >= 2 → free / num_ready (равномерно).
    """
    fm = float(free_margin)
    if fm <= 0:
        return 0.0
    n = max(1, int(num_ready))
    cap = max(0.0, min(float(cap_pct_of_free), 1.0))
    per = fm if n == 1 else fm / n
    per = min(per, fm * cap)          # предохранитель на одну сделку
    return round(max(0.0, per), 6)
